<?php # adalah tag pembuka dan penutup untuk menandai awal dan akhir blok kode PHP dalam sebuah file PHP.
$nilai = 80; # Pernyataan ini mendefinisikan variabel $nilai dan memberinya nilai 80.
if ($nilai >= 60) { # Ini adalah struktur pengkondisian. Pada kondisi ini, jika nilai dari variabel $nilai lebih besar atau sama dengan 60, maka blok kode di dalamnya akan dieksekusi.

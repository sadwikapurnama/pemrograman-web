} # Jika kondisi pada langkah sebelumnya terpenuhi (nilai $nilai lebih besar atau sama dengan 60), pesan "Anda Lulus dengan Nilai" akan ditampilkan diikuti dengan nilai dari variabel $nilai.
?>

<!-- Dalam konteks ini, karena nilai variabel $nilai adalah 80, yang lebih besar dari atau sama dengan 60, maka hasil yang akan ditampilkan adalah "Anda Lulus dengan Nilai 80". -->